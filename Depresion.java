    private void botonSumarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        int suma = obtenerValorSeleccionado(jComboBox1.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox2.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox3.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox4.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox5.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox6.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox7.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox8.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox9.getSelectedItem().toString()) +
            obtenerValorSeleccionado(jComboBox10.getSelectedItem().toString());
 String mensaje;
    if (suma >= 0 && suma <= 10) {
        mensaje = "Nivel de estrés bajo.";
    } else if (suma >= 11 && suma <= 20) {
        mensaje = "Nivel de estrés moderado.";
    } else if (suma >= 21 && suma <= 30) {
        mensaje = "Nivel de estrés alto.";
    } else {
        mensaje = "Nivel de estrés muy alto. ¡Busca ayuda!";
    }
    JOptionPane.showMessageDialog(this, mensaje);
    }                                          

